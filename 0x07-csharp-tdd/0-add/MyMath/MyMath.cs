using System;

namespace MyMath
{
    /// <summary>
    /// Represents mathematical operations.
    /// </summary>
    public class Operations
    {
        /// <summary>
        /// Computes sum of two ints.
        /// </summary>
        /// <param name="a">First int operand.</param>
        /// <param name="b">Second int operand.</param>
        /// <returns>The sum.</returns>
        public static int Add(int a, int b)
        {
            return a + b;
        }
    }
}
